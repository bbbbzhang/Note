// 练习11：打印三角形


// - 题目描述：
//   - 在控制台一次性打印形状如下：
//     ★★★★★★★★★★
//     ★★★★★★★★★
//     ★★★★★★★★
//     ★★★★★★★
//     ★★★★★★
//     ★★★★★
//     ★★★★
//     ★★★
//     ★★
//     ★
// - 题目提示：
// - 利用双重for循环
var a = '★'
var b =''
for(i=0;i<=5;i++){
    for(j=0;j<=5-i;j++){
        b+=a
    }
    b+='\n'
}
console.log(b);